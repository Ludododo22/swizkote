type TranslationKey = keyof typeof T.fr;

interface I18nCtx {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: TranslationKey) => string;
}

const I18nContext = createContext<I18nCtx | null>(null);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    return (localStorage.getItem("swiz_lang") as Lang) || "fr";
  });

  const setLang = (l: Lang) => {
    setLangState(l);
    localStorage.setItem("swiz_lang", l);
  };

  const t = (key: TranslationKey): string =>
    (T[lang] as any)[key] ?? (T.fr as any)[key] ?? key;

  return <I18nContext.Provider value={{ lang, setLang, t }}>{children}</I18nContext.Provider>;
}

/** Hook to force French locale in admin panel */
export function useForcedFrench() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useForcedFrench must be used within I18nProvider");
  const tFr = (key: TranslationKey): string =>
    (T.fr as any)[key] ?? key;
  return { ...ctx, t: tFr, lang: "fr" as Lang };
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used within I18nProvider");
  return ctx;
}

export function getDateLocale(lang: Lang) {
  if (lang === "de") return import("date-fns/locale/de").then(m => m.de);
  if (lang === "it") return import("date-fns/locale/it").then(m => m.it);
  if (lang === "en") return import("date-fns/locale/en-GB").then(m => m.enGB);
  return import("date-fns/locale/fr").then(m => m.fr);
}